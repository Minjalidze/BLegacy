    public static void SaveData()
    {
        try
        {
            File.WriteAllText(@"serverdata\cfg\BCore\overridearmor.cfg", JsonConvert.SerializeObject(OverrideArmor, Formatting.Indented));
            File.WriteAllText(@"serverdata\cfg\BCore\overridedamage.cfg", JsonConvert.SerializeObject(OverrideDamage, Formatting.Indented));
            
            Debug.Log("[BCore]: Server Config Saved!");
        }
        catch (Exception e)
        {
            Console.WriteLine(e);
            throw;
        }
    }

    public static void LoadData()
    {
        var fileArmor = File.ReadAllText(@"serverdata\cfg\BCore\overridearmor.cfg");
        var fileDamage = File.ReadAllText(@"serverdata\cfg\BCore\overridedamage.cfg");
        
        OverrideArmor = JsonConvert.DeserializeObject<Dictionary<string, int>>(fileArmor);
        OverrideDamage = JsonConvert.DeserializeObject<Dictionary<string, object>>(fileDamage);
        
